                              <h2>越南办公室开业</h2>
                              <p>继香港，新加坡，印尼办公室陆续搬迁，驻于越南胡志明市的办公室也于2017年7月26日正式开业，成为BIPO在亚太区的第六块战略版图。</p>
                              <p>本次活动， Suwandi先生（东南亚及大亚洲区域总监）亲临开业现场，同Quyen女士（BIPO运营经理），Thong先生（销售总监）以及其他同事等参与了越南办公室开业的剪彩仪式。</p>
                              <img src="images/news/2.png" alt="" class="img-responsive">
                              <p>Suwandi在剪彩仪式中发言表示：</p>
                              <p>“今天是非常特别的一天，BIPO越南办公室于今天正式开张成立了。从今往后，BIPO越南将在此积极展开商务活动，也希望能够获得大家的支持。我们持续关注和投资于先进技术，不断推进云服务范围和服务水平，无需前期投资就可让客户享有先进技术与贴心服务的完美体验。让我们一起努力，Make Life Easier。”</p>
                              <p>越南目前涉及业务有：</p>  
                              <ul>
                                 <li>- 人事代理</li>
                                 <li>- 薪资外包</li>
                                 <li>- 考勤自动化管理</li>
                                 <li>- BIPO平台</li>
                                 <li>- 招聘</li>
                                 <li>- 其他人事外包服务</li>
                              </ul>
                              <img src="images/news/3.png" alt="" class="img-responsive">
                              <p>BIPO，作为亚太区领先的一站式人力资源服务供应商，于2014年在上海创立，总部设在新加坡，研发中心成立于印度尼西亚，已在香港、台湾、澳门、泰国、澳大利亚、新西兰设立分公司。</p>
                              <p>随着越南办公室的开张成立，BIPO的东南亚战略版图逐渐清晰。我们同时也计划在2017年底前在日本、菲律宾、马来西亚等地设立办公室，完成整个亚太布局。此外，我们在当地聘请的优秀人才，实现了服务内容地域差别化，有力支持了境外客户的人力资源工作。</p>
                              <p>我们的服务产品包括人事代理、薪资外包、考勤自动化管理、劳动力精益管理、业务流程外包、差旅管理、弹性福利管理、外籍员工服务等等。除了人力资源服务，我们也提供行政和财务等相关的外包服务。</p>
                              <p>BIPO的优势在于通过BIPO HRMS“全模块”系统整合，实现云服务亚太地区“全覆盖”。</p>
                              <p>BIPO HRMS系统是BIPO自主开发并拥有自主知识产权的人力资源管理平台，拥有完整成熟的业务模块，包括员工生命周期管理、考勤自动化管理、薪资管理、弹性福利管理、行政管理、财务管理。同时，BIPO BI（商业智能）系统能够形成数据详实的报表，为客户精准分析及科学预测提供依据。BIPO平台适用于亚太所有国家和地区，齐全的功能，高度的集成，使繁杂的人力资源工作变得简洁。</p>
                              <p>中国大陆，港澳台，新加坡，印尼，泰国，以及本次新开业的越南，我们能看到BIPO正以成为亚太区服务网络最大，落地能力最强的人力资源一站式服务供应商为目标，一步步踏踏实实的朝向自己的目标努力前行，</p>
                              <p>与此同时，BIPO积极参与共建“一带一路”的愿景与行动，在东南亚大力拓展人力资源外包服务，这既是BIPO自身发展的需要，也是为广大客户提供延伸服务的需要。</p>
                              <p>BIPO的每一位同仁也正在努力做着行业的“工匠”，成为行业标杆并致力于把人力资源“中国服务”的精髓推向世界！</p>
                              <p>BIPO，期待与您更宽广的合作！</p>
